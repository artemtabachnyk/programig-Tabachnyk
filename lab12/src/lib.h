#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * @brief Обчислює комбінаційний коефіцієнт C(n, k).
 *
 * Функція обчислює комбінаційний коефіцієнт C(n, k), де n та k - цілі числа.
 *
 * @param n Ціле число, число елементів.
 * @param k Ціле число, кількість вибраних елементів.
 * @return Значення комбінаційного коефіцієнту C(n, k).
 */
