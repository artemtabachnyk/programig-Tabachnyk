/**
 * @brief Формує трикутник Паскаля рекурсивним методом.
 *
 * Функція формує трикутник Паскаля розміром n і виводить його на екран.
 *
 * @param n Ціле число, розмір трикутника Паскаля.
 */
void pascalTriangleRecursive(int n);


